package com.android.mohammadhf.thisorthat.model;

public class UserStatistic {
    private int Kol;
    private int Khas;
    private int Normal;
    private int BiAsar;

    public int getKol() {
        return Kol;
    }

    public void setKol(int kol) {
        Kol = kol;
    }

    public int getKhas() {
        return Khas;
    }

    public void setKhas(int khas) {
        Khas = khas;
    }

    public int getNormal() {
        return Normal;
    }

    public void setNormal(int normal) {
        Normal = normal;
    }

    public int getBiAsar() {
        return BiAsar;
    }

    public void setBiAsar(int biAsar) {
        BiAsar = biAsar;
    }
}
